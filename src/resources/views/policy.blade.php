<h1>Sekretesspolicy</h1>

<p>Pythonlabbet tar personlig integritet på allvar. Därför finns inga tracking cookies och Pythonlabbet lagrar endast nödvändig information. 
Med ett lärarkonto går det att skapa anonyma konton till en hel klass - det finns inget krav på att lämna uppgift om elevernas e-postadress eller riktigt namn.</p>

<h2>Insamling av personlig information</h2>
<p>Följande personlig information kan samlas in och lagras:</p>
<ol class="list-decimal pl-8">
    <li>information som du anger när du skapar ett konto på Pythonlabbet, till exempel namn och e-postadress;</li>
    <li>Kod som du skriver i inloggad läge. Koden sparas vid körning eller rättning för att du ska kunna se den senare;</li>
    <li>Förutom kod sparas resultatet om uppgiften är löst eller inte;</li>
    <li>Om en användare är kopplad till en klass sparas även lärarens ev. kommentarer om en uppgift för att vara tillgänglig för användaren;</li>
</ol>

<h2>Användning av personlig information</h2>
<p>Vi kan använda din personliga information för att:</p>
<ol class="list-decimal pl-8">
    <li>anpassa sajten åt dig;</li>
    <li>gör det möjligt för dig att använda funktionerna som finns på sajten;</li>
    <li>skicka information om nyheter via e-post (du kan enkelt meddela om du inte önskar nyheter)</li>
</ol>

<p class="mt-3">Vi kommer inte lämna ut personlig information till tredje part, förutom om vi är skyldiga att göra det enligt lag.</p>

<h2>Lagring av personlig information</h2>
<p>All personlig information lagras på servrar i Sverige. Det finns ingen bortre gräns för hur lång tid information lagras eftersom du ska kunna komma tillbaka och fortsätta när som helst. 
Kontakta oss för att ta bort ditt konto.</p>

<h2>Cookies</h2>
<p>Sessionscookies används för att kunna använda sajten som inloggad. I övrigt används youtube-nocookies.com för Youtube - därmed sparas inte tracking cookies för användare.
Pythonlabbet använder inget statistikverktyg (som exempelvis Google Analytics) på sajten eftersom de flesta inte respekterar användares integritet.
</p>

