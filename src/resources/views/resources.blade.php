<h1>Resurser</h1>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://www.pluggakuten.se/amne/programmering/python/">pluggakuten.se</a> 🇸🇪
<p class="ml-5">Pluggakuten har ett underforum för Python och här kan du relativt snabbt få hjälp med alla möjliga frågor. Även frågor på universitetsnivå.</p>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://projecteuler.net/">Project Euler</a> 🇺🇸
<p class="ml-5">På Project Euler finns hundratals problem som kräver matematiskt och datalogiskt tänkande. Problemen går att lösa med valfritt programmeringsspråk.
     Skapa ett konto för att kunna skicka in lösningar.</p>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://adventofcode.com/">Advent of Code</a> 🇺🇸
<p class="ml-5">Varje år sedan 2015 finns här en julkalender med väldigt trevliga problem. Varje dag 1-25 december kommer ett problem i två delar.
    Första veckan är lättast. I slutet utmanas även professionella programmerare. Alla gamla problem finns kvar.
</p>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://open.kattis.com/">open.kattis.com</a> 🇺🇸🇸🇪
<p class="ml-5">Kattis är ett domarsystem för programmeringstävlingar. Här finns mängder av programmeringsproblem med varierande svårighetsgrader. Från lätta till mycket svåra.
Du får poäng för lösta problem och det finns rankinglistor för län, länder och universitet. För att komma med på topp-50 i Sverige krävs mycket jobb! Sidans språk är engelska men sajten är från Sverige.</p>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://www.bebras.se/">bebras.se</a> 🇸🇪
<p class="ml-5">Databävern är en tävling i datalogiskt tänkande för olika åldrar dels i grundskolan och dels på gymnasiet.
    <ul class="list-none"><li><a target="_tab" class="text-green-500 hover:underline" href="http://legacy.bebras.se/">Gamla tävlingar</a></li></ul>
</p>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://csunplugged.org/en/">csunplugged.org</a> 🇳🇿
<p class="ml-5">På den här sidan finns många resurser för lärare, men även aktiviteter att göra hemma och utmaningar för elever. Språk: Scratch och Python. </p>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://hjernekraft.org/">hjernekraft.org</a> 🇸🇪
<p class="ml-5">Varje år hålls tävlingen First Lego League med legorobotar. En riktigt kul tävling som behöver mer uppmärksamhet på många håll i Sverige. Det är lätt att halka in på de norska delarna av sidan men det finns på svenska också.</p>

<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://hourofcode.com/se">hourofcode.com</a> 🇸🇪
<p class="ml-5">Hour of Code är en projekt för att få så många som möjligt att prova på programmering. Här hittar du många övningar på en timme för nybörjare.</p>


<a target="_tab" class="text-2xl text-green-500 hover:underline" href="http://ncm.gu.se/kanguru">Kängurutävlingen</a> 🇸🇪
<p class="ml-5">Kängurun är en tävling i matematik för grundskolan. Det är bra att träna sig i logiskt och spatial tänkande för att bli bra på programmering.
<ul class="list-none"><li><a target="_tab" class="text-green-500 hover:underline" href="http://ncm.gu.se/2664">Träningsmaterial</a></li></ul>
</p>


<a target="_tab" class="text-2xl text-green-500 hover:underline" href="http://progolymp.se/">progolymp.se</a> 🇸🇪
