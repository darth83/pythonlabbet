<a target="_tab" class="text-2xl text-green-500 hover:underline" href="https://scratch.mit.edu/">scratch.mit.edu</a> 🇸🇪
<p class="ml-5">Scratch är det mest populära programmeringsspråket med block. Att programmera med block är perfekt för nybörjare. 
Det kan vara bra att lära sig grunderna i Scratch före Python för de flesta.</p>

